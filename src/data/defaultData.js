export function mergeAppDataWithDefaults(partial) {
  if (!partial || typeof partial !== 'object') return defaultData;
  const merged = {
    ...defaultData,
    ...partial,
    settings: { ...defaultData.settings, ...(partial.settings || {}) },
  };

  // Sanitize newly added arrays to prevent runtime crashes from old saved data.
  if (!Array.isArray(merged.studentPortal)) merged.studentPortal = defaultData.studentPortal;
  if (!Array.isArray(merged.studentPortalStudents))
    merged.studentPortalStudents = defaultData.studentPortalStudents;

  return merged;
}

